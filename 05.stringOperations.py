'''
Declare a string as variable and perform string operation with the help of build in functions
'''

myString="Hi, this is Trinadh Sai, from India"

print(f'String in lower case \n{(myString.lower())}\n')

print(f'String in upper case \n{(myString.upper())}\n')

print(f'String titling \n{(myString.title())}\n')

print(f'\nString at indices 12-19 \n{(myString[12:19])}\n')

print(f'Spliting the string at ',')')

print(myString.split(','))