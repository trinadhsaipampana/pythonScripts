'''
Get 2 numbers from user and perform arithmetic operation
'''

num1=int(input("Provide first number:\n"))

num2=int(input("Provide second number:\n"))

print(f'Sum of 2 numbers is:\n{num1+num2}\n')

print(f'Difference of 2 numbers is:\n{num1-num2}\n')

print(f'Product of 2 numbers is:\n{num1*num2}\n')

print(f'Division of 2 numbers is:\n{num1/num2}\n')

print(f'Square of 10 is:\n{num1**2}\n')

print(f'Eliminating decimal value quotient:\n{num1//num2}')